%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------
value(Field, Record, Fields) ->
    [_RecAtom | RecVals] = tuple_to_list(Record),
    RecWithFields = lists:zip(Fields, RecVals),
    proplists:get_value(Field, RecWithFields, undefined).

