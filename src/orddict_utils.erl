-module(orddict_utils).

%% API
-export([get_value/2, get_value/3]).

%%%===================================================================
%%% API
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------

get_value(Key, Orddict) ->
    get_value(Key, Orddict, undefined).

get_value(Key, Orddict, Default) ->
    case orddict:find(Key, Orddict) of
        {ok, Val} -> Val;
        error -> Default
    end.

             
